#include<iostream>
using namespace std;
int main()
{
	short a = 10;
	short b = 5;
	
	// ==
	cout << (a == b) << endl;

	// !=
	cout << (a != b) << endl;

    // <
	cout << (a < b) << endl;

	// >
	cout << (a > b) << endl;

	// <=
	cout << (a <= b) << endl;

	// >=
	cout << (a >= b) << endl;

	return 0;
}