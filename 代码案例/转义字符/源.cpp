#include<iostream>
using namespace std;

int main()
{
//换行  \n
	
	cout << "Hello World" << endl;
	cout << "上面用endl;结束,下面用/n结束\n";
	cout << "Hello World\n";


//水平制表符 \t

	cout << "\n" << endl;
	cout << "x\tx" << endl;
	cout << "xx\tx" << endl;
	cout << "xxx\tx" << endl;
	cout << "xxxx\tx" << endl;
	cout << "xxxxx\tx" << endl;
	cout << "xxxxxx\tx" << endl;
	cout << "xxxxxxx\tx" << endl;
	cout << "以上都只输入了一个\\t" << endl;

//反斜杠  \\

	cout << "\n" << endl;
	cout << "要输入\\必须打出两个\\也就是\\\\\n";
	cout << "\n";

	system("pause");
}